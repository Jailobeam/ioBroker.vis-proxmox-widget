    instanceSelector: function (widAttr) {
        var values = [];
        var seen = {};

        Object.keys(vis.objects || {}).forEach(function (id) {
            var obj = vis.objects[id];
            if (!obj || obj.type !== 'instance' || !obj.common) {
                return;
            }
            if (!/^system\.adapter\.proxmox\.\d+$/.test(id)) {
                return;
            }

            var instanceId = id.substring('system.adapter.'.length);
            if (!seen[instanceId]) {
                seen[instanceId] = true;
                values.push(instanceId);
            }
        });

        values.sort(function (left, right) {
            return left.localeCompare(right, undefined, { numeric: true, sensitivity: 'base' });
        });

        return {
            input: '<input type="text" id="inspect_' + widAttr + '" class="vis-edit-textbox"/>',
            init: function () {
                $(this).autocomplete({
                    minLength: 0,
                    source: function (request, response) {
                        var term = String(request.term || '').toLowerCase();
                        response($.grep(values, function (value) {
                            return value.toLowerCase().indexOf(term) === 0;
                        }));
                    },
                    select: function (event, ui) {
                        $(this).val(ui.item.value);
                        $(this).trigger('change', ui.item.value);
                    }
                }).focus(function () {
                    $(this).autocomplete('search', '');
                });
            }
        };
    },

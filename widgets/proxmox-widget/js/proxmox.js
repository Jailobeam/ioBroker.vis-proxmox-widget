    normalizeConfiguredInstance: function (value) {
        var normalized = String(value || '').trim();
        if (!normalized || normalized === 'none' || normalized === 'auto') {
            return '';
        }
        return normalized;
    },
    resolveCardsPerRow: function (data) {
        if (data.cardsPerRow !== undefined && data.cardsPerRow !== null && data.cardsPerRow !== '') {
            return Math.max(1, parseInt(data.cardsPerRow, 10) || 2);
        }
        if (data.layout === 'list') {
            return 1;
        }

        return Math.max(1, parseInt(data.columns, 10) || 2);
    },
    initWidgetState: function ($widget, settings) {
        var current = $widget.data('proxmoxState');
        if (current) {
            vis.binds.proxmox.cleanupWidget($widget, current);
        }

        var state = {
            settings: settings,
            boundIds: [],
            requestToken: 0,
            onStateChange: function () {
                vis.binds.proxmox.scheduleRefresh($widget);
            },
            scheduledRefresh: null,
            refreshTimer: null,
            globalClickHandler: null,
        };

        state.refreshTimer = setInterval(function () {
            if (!document.body.contains($widget[0])) {
                vis.binds.proxmox.cleanupWidget($widget, state);
                return;
            }
            vis.binds.proxmox.refreshWidget($widget);
        }, 30000);

        state.globalClickHandler = function (event) {
            var currentMenu = vis.binds.proxmox._floatingMenu;
            if (currentMenu && currentMenu.element && currentMenu.element[0].contains(event.target)) {
                return;
            }
            if (!$widget[0].contains(event.target)) {
                vis.binds.proxmox.closeFloatingMenu();
            }
        };
        document.addEventListener('click', state.globalClickHandler);

        $widget.data('proxmoxState', state);
    },
    cleanupWidget: function ($widget, state) {
        if (!state) {
            return;
        }

        if (state.scheduledRefresh) {
            clearTimeout(state.scheduledRefresh);
        }
        if (state.refreshTimer) {
            clearInterval(state.refreshTimer);
        }
        if (state.globalClickHandler) {
            document.removeEventListener('click', state.globalClickHandler);
        }

        vis.binds.proxmox.closeFloatingMenu();
        vis.binds.proxmox.updateBindings(state, []);
        $widget.removeData('proxmoxState');
    },
    scheduleRefresh: function ($widget) {
        var widgetState = $widget.data('proxmoxState');
        if (!widgetState || widgetState.scheduledRefresh) {
            return;
        }

        widgetState.scheduledRefresh = setTimeout(function () {
            widgetState.scheduledRefresh = null;
            vis.binds.proxmox.refreshWidget($widget);
        }, 200);
    },
    refreshWidget: function ($widget) {
        var widgetState = $widget.data('proxmoxState');
        if (!widgetState) {
            return;
        }

        var settings = widgetState.settings;
        var requestToken = ++widgetState.requestToken;

        vis.binds.proxmox.fetchAvailableInstances(function (instances) {
            var latestState = $widget.data('proxmoxState');
            if (!latestState || latestState.requestToken !== requestToken) {
                return;
            }

            var resolution = vis.binds.proxmox.resolveActiveInstance(settings.proxmoxInstance, instances);
            settings.emptyText = resolution.emptyText;

            if (!resolution.instance) {
                vis.binds.proxmox.updateBindings(latestState, []);
                vis.binds.proxmox.render($widget, [], settings);
                return;
            }

            vis.binds.proxmox.fetchAdapterItems(resolution.instance, settings, function (result) {
                var newestState = $widget.data('proxmoxState');
                if (!newestState || newestState.requestToken !== requestToken) {
                    return;
                }

                vis.binds.proxmox.updateBindings(newestState, result.watchIds);
                vis.binds.proxmox.render($widget, result.items, settings);
            });
        });
    },
    fetchAvailableInstances: function (callback) {
        if (vis.conn && vis.conn._socket) {
            vis.conn._socket.emit(
                'getObjectView',
                'system',
                'instance',
                { startkey: 'system.adapter.proxmox.', endkey: 'system.adapter.proxmox.\u9999' },
                function (err, res) {
                    var rows = res && res.rows ? res.rows : [];
                    var instances = rows
                        .map(function (row) {
                            return row && row.id ? row.id.replace(/^system\.adapter\./, '') : '';
                        })
                        .filter(function (id) {
                            return /^proxmox\.\d+$/.test(id);
                        });

                    callback(vis.binds.proxmox.unique(instances));
                }
            );
            return;
        }

        callback([]);
    },
    resolveActiveInstance: function (configuredInstance, availableInstances) {
        var available = availableInstances || [];
        var selected = vis.binds.proxmox.normalizeConfiguredInstance(configuredInstance);

        if (selected === 'proxmox.0' && available.indexOf(selected) === -1) {
            selected = '';
        }

        if (!selected) {
            if (available.length === 1) {
                return {
                    instance: available[0],
                    emptyText: 'Keine Proxmox-Daten',
                };
            }
            return {
                instance: '',
                emptyText: available.length ? 'Bitte Proxmox-Instanz eintragen' : 'Keine Proxmox-Instanz vorhanden',
            };
        }

        if (available.indexOf(selected) === -1) {
            return {
                instance: '',
                emptyText: 'Proxmox-Instanz nicht gefunden',
            };
        }

        return {
            instance: selected,
            emptyText: 'Keine Proxmox-Daten',
        };
    },
    updateBindings: function (widgetState, watchIds) {
        var desired = {};

        $.each(watchIds || [], function (_, id) {
            if (id) {
                desired[id] = true;
            }
        });

        $.each(widgetState.boundIds || [], function (_, id) {
            if (!desired[id]) {
                vis.states.unbind(id, widgetState.onStateChange);
            }
        });

        $.each(Object.keys(desired), function (_, id) {
            if ((widgetState.boundIds || []).indexOf(id) === -1) {
                vis.states.bind(id, widgetState.onStateChange);
            }
        });

        widgetState.boundIds = Object.keys(desired);
    },
    render: function ($widget, items, settings) {
        var $title = $widget.find('.proxmox-widget__title');
        var $summary = $widget.find('.proxmox-widget__summary');
        var $body = $widget.find('.proxmox-widget__body');

        $widget.css('zoom', String(settings.scale / 100));
        vis.binds.proxmox.applyTheme($widget, settings);
        $title.text(settings.title);
        $body.css('grid-template-columns', 'repeat(' + settings.cardsPerRow + ', minmax(0, 1fr))');
        $body.empty();

        if (!items || !items.length) {
            $summary.text(settings.showSummary ? settings.emptyText : '');
            $('<div class="proxmox-widget__empty"></div>').text(settings.emptyText).appendTo($body);
            return;
        }

        $summary.text(settings.showSummary ? vis.binds.proxmox.buildSummary(items, settings) : '');

        $.each(items, function (_, item) {
            $body.append(vis.binds.proxmox.renderCard($widget, item, settings));
        });
    },

    buildSurfaceBackground: function (mode, color1, color2, direction, opacity) {
        if (mode === 'transparent') {
            return 'transparent';
        }

        var startColor = vis.binds.proxmox.applyAlpha(color1, opacity);
        if (mode === 'color') {
            return startColor;
        }

        var endColor = vis.binds.proxmox.applyAlpha(color2 || color1, opacity);
        return 'linear-gradient(' + (direction || '180deg') + ', ' + startColor + ', ' + endColor + ')';
    },
    buildShadow: function (strength) {
        var percent = vis.binds.proxmox.readPercent(strength, 0);
        var alpha;
        var blur;
        var offsetY;

        if (!percent) {
            return 'none';
        }

        alpha = Math.min(0.38, percent / 260);
        blur = Math.round(8 + (percent * 0.24));
        offsetY = Math.round(2 + (percent * 0.08));

        return '0 ' + offsetY + 'px ' + blur + 'px rgba(0, 0, 0, ' + alpha.toFixed(3) + ')';
    },
